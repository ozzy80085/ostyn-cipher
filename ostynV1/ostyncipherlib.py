def enc(text):
	text.replace("a", "~")
	text.replace("b", "%")
	text.replace("c", "[")
	text.replace("d", "`")
	text.replace("e", "(")
	text.replace("f", "{")
	text.replace("g", "?")
	text.replace("h", "|")
	text.replace("i", ";")
	text.replace("j", ":")
	text.replace("k", "&")
	text.replace("l", "-")
	text.replace("m", "@")
	text.replace("n", "$")
	text.replace("o", ".")
	text.replace("p", ")")
	text.replace("q", "“")
	text.replace("r", ">")
	text.replace("s", "+")
	text.replace("t", "/")
	text.replace("u", "]")
	text.replace("v", "}")
	text.replace("w", "!")
	text.replace("x", "_")
	text.replace("y", "#")
	text.replace("z", "\\")
